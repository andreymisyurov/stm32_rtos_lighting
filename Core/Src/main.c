/* USER CODE BEGIN 4 */
void LedFadeTask(void const * argument) {
    uint16_t pwmValue = 0;
    int step = 1500; // шаг изменения коэффициента заполнения

    // Запуск ШИМ на каналах
    HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
    HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);

    for (;;) {
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, pwmValue);
        __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, pwmValue);

        pwmValue += step;
        if (pwmValue < 1500 || pwmValue >= 64035) {
            step = -step; // Меняем направление изменения яркости
        }

        osDelay(50); // Регулируйте это значение для изменения скорости затухания/возгорания
    }

}

void LedBlinkTask(void const * argument) {
  for(;;) {
	  HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_3);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, !(GPIOA->ODR & GPIO_PIN_3));
	  osDelay(1000);
  }
}

void SwitchPinMode(GPIO_TypeDef* GPIOx, uint16_t GPIO_Pin, uint32_t mode) {
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    // Очистить старую конфигурацию пина
    HAL_GPIO_DeInit(GPIOx, GPIO_Pin);

    // Настроить пин в соответствии с выбранным режимом
    GPIO_InitStruct.Pin = GPIO_Pin;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;

    if (mode == GPIO_MODE_OUTPUT_PP) {
        GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
        HAL_GPIO_Init(GPIOx, &GPIO_InitStruct);
    } else if (mode == GPIO_MODE_AF_PP) {
        GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
        HAL_GPIO_Init(GPIOx, &GPIO_InitStruct);
    }
}

void applyMode(LedMode mode) {
	if(0 == mode) {
		osThreadSuspend(ledBlinkTaskHandle);
		osThreadSuspend(ledFadeTaskHandle);
		SwitchPinMode(GPIOA, GPIO_PIN_2, GPIO_MODE_OUTPUT_PP);
		SwitchPinMode(GPIOA, GPIO_PIN_3, GPIO_MODE_OUTPUT_PP);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_RESET);
	} else if(1 == mode) {
		osThreadSuspend(ledBlinkTaskHandle);
		osThreadSuspend(ledFadeTaskHandle);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_SET);
	} else if(2 == mode) {
		osThreadSuspend(ledFadeTaskHandle);
		osThreadResume(ledBlinkTaskHandle);
	} else if(3 == mode) {
		osThreadSuspend(ledBlinkTaskHandle);
		SwitchPinMode(GPIOA, GPIO_PIN_2, GPIO_MODE_AF_PP);
		SwitchPinMode(GPIOA, GPIO_PIN_3, GPIO_MODE_AF_PP);
        osThreadResume(ledFadeTaskHandle);
	}
}

void ButtonTask(void const * argument) {
  for(;;) {
	  applyMode(led_mode);
	  osDelay(500);
  }

}

void SendDataTask(void const * argument) {
	uint16_t analogValue = 0;
	char buffer[64] = {0};
	for(;;) {
		strcpy(buffer, "Gabella\r\n\0");
		HAL_ADC_Start(&hadc1);
		if (HAL_ADC_PollForConversion(&hadc1, 1000) == HAL_OK) {
			analogValue = HAL_ADC_GetValue(&hadc1);
			sprintf(buffer, "Mode is %d, Soil is %u\r\n", (int)led_mode, analogValue);
		}
		HAL_ADC_Stop(&hadc1);
		HAL_UART_Transmit(&huart1, (uint8_t *)buffer, strlen(buffer), HAL_MAX_DELAY);
		osDelay(2000);
	}
}
